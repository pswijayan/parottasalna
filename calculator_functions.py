#Addition
def add(num1:float, num2:float):
    return num1 + num2, "+"


#Substraction 
def sub(num1:float, num2:float):
    return num1 - num2, "-"


#Multipilication  
def mul(num1:float, num2:float):
    return num1 * num2, "x"


#Division  
def div(num1:float, num2:float):
    return num1 / num2, "/"


#MOD
def mod(num1:float, num2:float):
    return num1 % num2, "%"


#POWER
def pow(num1:float, num2:float):
    return num1 ** num2, "^"




